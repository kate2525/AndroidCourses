package com.example.kate.lesson5;


public class MyLink {

    private String name;
    private String link;


    public void setName(String name) {
        this.name = name;
    }

    public void setLink(String link) {
        this.link = link;
    }

    public String getName() {
        return name;
    }

    public String getLink() {
        return link;
    }
}
